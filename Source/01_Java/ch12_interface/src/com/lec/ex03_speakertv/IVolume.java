package com.lec.ex03_speakertv;
// 인터페이스 - static final 변수(상수)와 추상메소드, (예외: default method, static method)
// 작업명세서/ 다형성 / 객체 부품화 작업 도움
public interface IVolume {
	public void volumeUp(); // 볼륨 레벨을 1씩 up
	public void volumeUp(int level); // 볼륨 레벨을 level씩 up
	public void volumeDown(); // 볼륨 레벨을 1씩 down
	public void volumeDown(int level); // 볼륨 레벨을 level씩 down
	public default void setMute(boolean mute) { // default method(실무 개발자는 안 씀)
		if(mute) {
			System.out.println("무음");
		}else {
			System.out.println("무음해제");
		}
	}
	public static void changeBattery() { // static method(실무 개발자는 안 씀)
		System.out.println("건전지를 교환합니다.");
	}
}
